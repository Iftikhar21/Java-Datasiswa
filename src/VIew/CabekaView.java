    private void loadData() {
         String tgl = "";
         if (itemTgl.getDate() != null) {
             SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
             tgl = sdf.format(itemTgl.getDate());
         }

         String[] args = {
             itemNIS.getText(),
             tgl
         };

         tblBk.setModel(dao.getModelAllCabeka(args));
         scrCabeka.setViewportView(tblBk);
    }
    
    private void loadDataTf(String [] data) {
        if (data.length < 3) {
            System.out.println("Invalid data array length.");
            return;
        }

        Cabeka cabeka = new Cabeka();
        cabeka.setNis(data[0]);

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        try {
            java.util.Date utilDate = sdf.parse(data[2]);

            java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());

            cabeka.setTgl(sqlDate);

        } catch (ParseException e) {
            System.out.println("Date parsing error: " + e.getMessage());
            return;
        }

        this.cabeka = dao.getSingleCabeka(cabeka);

        if (this.cabeka == null) {
            System.out.println("Cabeka not found for NIS: " + data[0]);
            return;
        }
        
        itemNIS.setText(this.cabeka.getNis());
        itemPermasalahan.setText(this.cabeka.getProblem());
        itemSolusi.setText(this.cabeka.getSolution());
        itemGuruBk.setText(this.cabeka.getKdGuruBk());
        kehadiran.setSelectedItem(this.cabeka.getKehadiran());
        itemTgl.setDate(this.cabeka.getTgl());

        itemNamaSiswa.setText(this.cabeka.getSiswa().getNama());
        tfGuruBk.setText(this.cabeka.getGuru().getNama());
    }
    
     private void removeInput() {
        for (JTextField textField : textFields) {
            textField.setText("");
        }
        
        for (JTextArea textArea : textArea) {
            textArea.setText("");
        }

        if (kehadiran != null) {
            kehadiran.setSelectedItem(null); 
        }

        if (itemTgl != null) {
            itemTgl.setDate(null);
        }
    }
     
     public boolean checkFill() {
        for (JTextField textField : textFields) {
            if (textField.getText().isEmpty()) {
                return false;
            }
        }
        
        for (JTextArea textArea : textArea) {
            if (textArea.getText().isEmpty()) {
                return false;
            }
        }
        
        if (kehadiran.getSelectedItem() == null) {
            return false;
        }
        if (itemTgl.getDate() == null) {
            return false;
        }
        return true;
    }

