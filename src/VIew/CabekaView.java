    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        if (checkFill() == false) {
            JOptionPane.showMessageDialog(this, "Anda belum menginput semuanya");
            return;
        }
        
        if (JOptionPane.showConfirmDialog(null, "Data akan disimpan. Lanjutkan?", "Konfirmasi Simpan Data", 
            JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION) {
            return;
        }

        Cabeka cabeka = new Cabeka();
        cabeka.setNis(itemNIS.getText());
        cabeka.setProblem(itemPermasalahan.getText());
        cabeka.setSolution(itemSolusi.getText());
        cabeka.setKehadiran(kehadiran.getSelectedItem().toString());
        cabeka.setKdGuruBk(itemGuruBk.getText());
        cabeka.setTgl(new java.sql.Date(itemTgl.getDate().getTime()));

        dao.saveCabeka(cabeka);
        loadData();
        removeInput();
    }//GEN-LAST:event_btnSimpanActionPerformed

